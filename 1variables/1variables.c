#include <stdio.h>
int main()
{
    int a = 4;
    // int b = 8.5; 8.5 is not an integer
    float b = 8.5;
    char c = 'A';
    int d = 45;
    printf("The value of a is %d \n", a);
    printf("The value of b is %f \n", b);
    printf("The value of c is %c \n", c);
    printf("Sum of a and d is %d", a + d);
    return 0;
}

